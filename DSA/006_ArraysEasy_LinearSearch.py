########################################################################################################################
# Optimal Solution
# ----------------------------------------------------------------------------------------------------------------------
# Time  Complexity: O(n)
# Space Complexity: O(1)
########################################################################################################################


def linear_search_OPT(arr, k):
    n = len(arr)
    
    for i in range(0, n):
        if arr[i] == k:
            return True
    
    return False


arr = [1, 0, 2, 3, 2, 0, 0, 4, 5, 1]
print(f'arr = {arr}')

print(f'is 4 present? {linear_search_OPT(arr, 4)}')
print(f'is 7 present? {linear_search_OPT(arr, 7)}')
