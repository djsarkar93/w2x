########################################################################################################################
# Largest Element Of An Array
########################################################################################################################


########################################################################################################################
# Optimal Solution
# ----------------------------------------------------------------------------------------------------------------------
# Time  Complexity: O(n)
# Space Complexity: O(1)
########################################################################################################################


def find_largest(arr):
    n = len(arr)
    maxi = float('-inf')            # mini = float('inf')
    for i in range(0, n):
        maxi = max(arr[i], maxi)    # mini = min(arr[i], mini)
    return maxi


arr = [2, 5, 1, 3, 0]
print(f'arr = {arr}')
result = find_largest(arr)
print(f'Largest = {result}')
